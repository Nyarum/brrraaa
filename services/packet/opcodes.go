func ConstructPacket(obj interface{}) (res []byte, err error) {
	var (
		buf    = bytes.NewBuffer([]byte{})
		opcode int
	)

	switch v := obj.(type) {
	case OutcomingAuth:
		opcode = OP_SERVER_LOGIN

		res, err = constructAuth(v)
	}

	err = writeGeneric(buf, uint16(len(res)))
	err = writeGeneric(buf, uint16(opcode))
	err = writeGeneric(buf, res)

	return
}

func DeconstructPacket(opcode int, data []byte) (res interface{}, err error) {
	switch opcode {
	case OP_CLIENT_LOGIN:
		res, err = deconstructAuth(data)
	}

	return
}
